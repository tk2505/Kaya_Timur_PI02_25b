# Множество гласных латиницы в обоих регистрах для простоты демонстрации
VOWELS = set("aeiouAEIOU")  # Набор гласных символов, по которому выполняется быстрое членство за O(1) на проверку 

def count_vowels(s):  # Объявляем рекурсивную функцию с одним параметром строки s 
    # Базовый случай: пустая строка содержит 0 гласных (условие завершения рекурсии) 
    if s == "":  # Проверяем базовый случай, чтобы прекратить дальнейшие рекурсивные вызовы 
        return 0  # Возвращаем константный результат на базовом уровне без рекурсии 
    # Рекурсивный случай: считаем вклад первого символа и рекурсивно обрабатываем хвост 
    first = 1 if s[0] in VOWELS else 0  # Вычисляем вклад первой позиции в ответ (1, если гласная) 
    return first + count_vowels(s[1:])  # Суммируем локальный вклад с результатом на подстроке s[1:] (меньшая подзадача) 

# Пример использования: произвольная тестовая строка
text = "Recursion is powerful!"  # Тестовая строка для демонстрации корректности решения [attached_file:1]
print(count_vowels(text))  # Ожидается целое число — количество гласных в заданной строке [attached_file:1]
