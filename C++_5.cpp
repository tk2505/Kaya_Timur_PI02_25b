#include <iostream>  // Подключаем ввод/вывод для демонстрации результатов
#include <string>    // Подключаем std::string для работы со строкой
using namespace std; // Упрощаем обращение к именам стандартной библиотеки

// Рекурсивная функция подсчёта гласных в строке s, начиная с позиции i 
int countVowels(const string& s, size_t i = 0) { // Параметр i — индекс текущего символа, по умолчанию 0 
    if (i == s.size()) { // Базовый случай: вышли за последний символ — возвращаем 0 
        return 0;        // Завершение рекурсии с константным результатом 
    }
    char c = s[i]; // Берём текущий символ для проверки 
    bool isVowel = (c=='a'||c=='e'||c=='i'||c=='o'||c=='u'|| // Проверка на гласную (латиница, нижний регистр) 
                    c=='A'||c=='E'||c=='I'||c=='O'||c=='U'); // Проверка на гласную (латиница, верхний регистр) 
    return (isVowel ? 1 : 0) + countVowels(s, i + 1); // Вклад текущего символа + рекурсивный подсчёт хвоста 
}

int main() {                                // Точка входа в программу 
    string text = "Recursion is powerful!"; // Пример строки для демонстрации 
    cout << countVowels(text) << endl;      // Выводим результат подсчёта гласных 
    return 0;                                // Возвращаем код успешного завершения 
}
// вывод: 8