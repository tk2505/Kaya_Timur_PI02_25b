public class Main { // Объявление публичного класса Main — стандартная точка входа
    static boolean isVowel(char c) { // Вспомогательный метод: проверяет, является ли символ гласной
        return "aeiouAEIOU".indexOf(c) >= 0; // Ищем символ в строке гласных; indexOf >= 0 означает «нашли» 
    } // Закрывающая скобка метода isVowel 

    static int countVowels(String s, int i) { // Рекурсивная функция подсчёта гласных в s, начиная с индекса i 
        if (i == s.length()) { // Базовый случай: достигнут конец строки — дальше гласных нет 
            return 0; // Возвращаем 0 на базовом случае, завершая текущую ветвь рекурсии 
        } // Закрываем ветку if для базового случая 
        int first = isVowel(s.charAt(i)) ? 1 : 0; // Локальный вклад: 1, если текущий символ — гласная, иначе 0 
        return first + countVowels(s, i + 1); // Рекурсивный шаг: суммируем вклад с результатом для подстроки, начиная с i+1 
    } // Конец определения рекурсивной функции countVowels 
    public static void main(String[] args) { // Точка входа приложения: JVM запускает метод main 
        String text = "Recursion is powerful!"; // Пример строки для демонстрации работы алгоритма 
        System.out.println(countVowels(text, 0)); // Выводим количество гласных, начиная подсчёт с позиции 0 
    } // Конец метода main 
} // Конец класса Main 
// вывод: 8